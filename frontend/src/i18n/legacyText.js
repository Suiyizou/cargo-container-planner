function translateReportTerms(value) {
  return String(value || "")
    .replace(/长×宽/g, "L x W")
    .replace(/宽×高/g, "W x H")
    .replace(/长×高/g, "L x H")
    .replace(/长/g, "L")
    .replace(/宽/g, "W")
    .replace(/高/g, "H");
}

function translateCore(text, locale) {
  if (locale === "zh-CN") return text;
  if (exactEn[text]) return exactEn[text];
  for (const [pattern, replacement] of patternEn) {
    if (pattern.test(text)) return text.replace(pattern, replacement);
  }
  return text;
}

export function translateLegacyText(value, locale = getCurrentLocale()) {
  if (value == null) return value;
  const text = String(value);
  if (!text || locale === "zh-CN") return text;
  const match = text.match(/^(\s*)([\s\S]*?)(\s*)$/);
  const leading = match?.[1] || "";
  const core = match?.[2] || text;
  const trailing = match?.[3] || "";
  return `${leading}${translateCore(core, locale)}${trailing}`;
}

export function localizeCanvasContext(ctx, locale = getCurrentLocale()) {
  if (!ctx || ctx.__cargoPlannerI18nPatched) return ctx;
  const fillText = ctx.fillText.bind(ctx);
  const strokeText = ctx.strokeText.bind(ctx);
  const measureText = ctx.measureText.bind(ctx);
  const translateText = (text) => translateLegacyText(text, locale);
  ctx.fillText = (text, ...args) => fillText(translateText(text), ...args);
  ctx.strokeText = (text, ...args) => strokeText(translateText(text), ...args);
  ctx.measureText = (text, ...args) => measureText(translateText(text), ...args);
  ctx.__cargoPlannerTranslateText = translateText;
  ctx.__cargoPlannerLocale = locale;
  ctx.__cargoPlannerI18nPatched = true;
  return ctx;
}
